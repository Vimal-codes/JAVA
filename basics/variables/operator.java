package basics.variables;

public class operator {
    public static void main(String[] args) {
        // = assignment operator
        // =,-,*,/
        int a=10;
        int b=20;
        System.out.println(a+b);
        System.out.println(b-a);
        System.out.println(a*b);
        System.out.print(b/a);
    }
}
